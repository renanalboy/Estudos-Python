from numpy import*

print("For -> vetor como entrada")
v = [1,2,3,4,5]
for x in v:
    print(x)

print("For -> utilizando um parametro")
for i in range(5):
    print(i)


print("For -> utilizando dois parametros")
for i in range(1, 5):
    print(i)

print("For -> utilizando 3 parametros")
for i in range(0, 5, 2):
    print(i)
